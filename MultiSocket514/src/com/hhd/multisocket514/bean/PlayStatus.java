package com.hhd.multisocket514.bean;

public class PlayStatus {
	
	private String IP;
	private String leftSourceIP;
	private String leftTrack;
	private String rightSourceIP;
	private String rightTrack;
	
	public String getIP() {
		return IP;
	}
	public void setIP(String iP) {
		IP = iP;
	}
	public String getLeftSourceIP() {
		return leftSourceIP;
	}
	public void setLeftSourceIP(String leftSourceIP) {
		this.leftSourceIP = leftSourceIP;
	}
	public String getLeftTrack() {
		return leftTrack;
	}
	public void setLeftTrack(String leftTrack) {
		this.leftTrack = leftTrack;
	}
	public String getRightSourceIP() {
		return rightSourceIP;
	}
	public void setRightSourceIP(String rightSourceIP) {
		this.rightSourceIP = rightSourceIP;
	}
	public String getRightTrack() {
		return rightTrack;
	}
	public void setRightTrack(String rightTrack) {
		this.rightTrack = rightTrack;
	}
	
	

}
